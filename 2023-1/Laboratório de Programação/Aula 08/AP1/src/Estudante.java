public class Estudante {
    private String nome;
    private int idade;
    private int pontos;

    public String getNome(){
        return this.nome;
    }
    public int getIdade(){
        return this.idade;
    }
    public int getPontos(){
        return this.pontos;
    }
    public String setNome(){
        this.nome = nome;
    }
    public int setIdade(){
        this.idade = idade;
    }
    public int setPontos(){
        this.pontos = pontos;
    }
}
